let myArr = ["keshav", "singh", "sunny", "singh"]

let arr = 0

while (arr < myArr.length) {
    console.log(`Array name is ${myArr[arr]}`);
    arr = arr + 1
}

// Array name is keshav
// Array name is singh
// Array name is sunny
// Array name is singh

